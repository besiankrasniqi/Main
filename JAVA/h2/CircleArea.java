
public class CircleArea {

	public static void main (String[] args) {
	
	int circleRadius;
	double valueofPi;
	double circleArea;
	
	circleRadius = 5;
	valueofPi = 3.1425;

	circleArea = valueofPi * circleRadius * circleRadius;

	System.out.println("The Area of the Circle with radius: " + circleRadius + " is " + circleArea);

	

	}


}